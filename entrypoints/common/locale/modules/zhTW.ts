export function toZhTW<T extends Record<string, string>>(messages: T): T {
  return Object.fromEntries(
    Object.entries(messages).map(([key, value]) => {
      return [
        key,
        zhTWReplacements.reduce((text, [pattern, replacement]) => {
          return text.replace(pattern, replacement);
        }, value),
      ];
    }),
  ) as T;
}

export default toZhTW;
